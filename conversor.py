# Converter Celsius em Fahrenheit
# Para converter Celsius em Fahrenheit Multiplique o valor em celsius por 1.8 e adicione 32.
# Para converter Fahrenheit em Celsius: o valor de Fahrenheit - 32 dividido por 1.8 

celsius = 25
fahrenheit = (celsius * 1.8) + 32
print (fahrenheit)

fahrenheit_ = 77
celsius_ = (fahrenheit_ - 32) / 1.8
print(celsius_)